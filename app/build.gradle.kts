  // Automatically decode debug.keystore from debug.keystore.base64 if it does not exist.
  // This ensures that builds on GitHub Actions or other CI/CD environments succeed out of the box.
  val debugKeystoreFile = file("${rootDir}/debug.keystore")
  if (!debugKeystoreFile.exists()) {
    val base64File = file("${rootDir}/debug.keystore.base64")
    if (base64File.exists()) {
      try {
        val base64Text = base64File.readText().trim()
        val decodedBytes = Base64.getDecoder().decode(base64Text)
        debugKeystoreFile.writeBytes(decodedBytes)
        logger.lifecycle("Successfully restored debug.keystore from debug.keystore.base64!")
      } catch (e: Exception) {
        logger.error("Failed to restore debug.keystore: ${e.message}")
      }
    }
  }

